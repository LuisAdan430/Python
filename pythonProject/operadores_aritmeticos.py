#pedir numeros al usuario
numero1 = int(input("Ingresa un numero (1): "))
numero2 = int(input("Ingresa un numero (2): "))
#imprimir resultados
#Suma
print(f'La suma es {numero1 + numero2}')
#resta
print(f'La resta es {numero1 - numero2}')
#multiplicacion
print(f'La multiplicacion es {numero1 * numero2}')
#division
print(f'La division es {numero1 / numero2}')
#modulo
print(f'La modulo es {numero1 % numero2}')